#!/usr/bin/python3

def divisible_by_2(my_list=[]):
    """Finds all multiples of 2
    Args:
        my_list:  list to check
    Returns:
        a new list with True of Fals
    """
    new_list
    for element in my_list:
        if element % 2 == 0:
            new_list.append(True)
        else:
            new_list.append(False)
